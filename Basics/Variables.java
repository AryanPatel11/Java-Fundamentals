package Basics;
public class Variables {
    public static void main(String[] args) {
        //variables declaration
        int a = 10;
        int b = 25;
        int sum = a + b;
        int dif = a - b;
        int mul = a * b;

        //printing the output
        System.out.println("The sum of variable a and b: a + b = " + sum);
        System.out.println("The difference between variables a and b: b - a = " + dif);
        System.out.println("The multiplication of a and b: a * b = " + mul);
    }
}